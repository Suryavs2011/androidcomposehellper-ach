fun GenerateColorFromARGB(a: Int, r: Int, g: Int, b: Int): Color {
val color = Color(alpha = a / 255f, red = r / 255f, green = g / 255f, blue = b / 255f)
return color
}

fun GenerateColorVariations(
base: Color,
steps: Int = 10,
alphaSteps: Int = 5
): List<Color> {
val colors = mutableListOf<Color>()
val r = (base.red * 255).roundToInt()
val g = (base.green * 255).roundToInt()
val b = (base.blue * 255).roundToInt()
for (i in 1..steps) {
val factor = i.toFloat() / steps
colors.add(Color(
red = (r * (1 - factor) / 255f),
green = (g * (1 - factor) / 255f),
blue = (b * (1 - factor) / 255f)
))
colors.add(Color(
red = (r + (255 - r) * factor) / 255f,
green = (g + (255 - g) * factor) / 255f,
blue = (b + (255 - b) * factor) / 255f
))
}
for (i in 1..alphaSteps) {
val alpha = i.toFloat() / alphaSteps
colors.add(Color(red = r / 255f, green = g / 255f, blue = b / 255f, alpha = alpha))
}
return colors
}

